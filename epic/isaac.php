			<h4>Use Case</h4>
			<p>After his school announced its upcoming science fair, Isaac began seriously thinking about doing a project about Mars. He did a Google search and found many results; however, the websites he found were too complicated for him to use effectively. He asked his science teacher to help him out. During class, they worked together and finally discovered the "Team Curiosity" website, which upon loading, displayed recent highlights, a sidebar with weather data, and two navigation links. With a single click from the main page, they could access a photo gallery and a feed of detailed but easily readable articles on Curiosity's latest discoveries. They were able to easily get the exact information that Isaac needed for his project without digging through a complex website. The simplicity of the interface made it intuitive to both of them, even with Isaac's limited computer experience. They also enjoyed the realtime updates, which kept Isaac coming back to the site even after the science fair was over.</p>
